#include "libft.h"

/* Функция побайтно сравнивает коды символов двух строк, на которые 
указывают аргументы функции. Сравнение прекращается если встретились 
отличающиеся символы. возвращает 0 если строки равны, отрицательное
значение если s2 больше s1 и положительное если наоборот. */

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while (i < n - 1 && s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
